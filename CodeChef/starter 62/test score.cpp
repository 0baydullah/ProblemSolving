using namespace std;

void miryoku()
{
    int n,x,y;
    int flag=0;
    cin >> n >> x >> y;
    for(int i=0 ; i<=n ; i++){
        if(i*x==y){ flag=1;
        break;}
    }
    if(flag) cout << "YES\n";
    else cout << "NO\n";
}

